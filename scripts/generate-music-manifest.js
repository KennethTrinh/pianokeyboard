const fs = require('fs');
const path = require('path');

const musicDir = path.join(__dirname, '..', 'src', 'music');
const outputFile = path.join(musicDir, 'manifest.js');

const MIDI_EXTENSIONS = /\.(mid|midi)$/i;

const files = fs.readdirSync(musicDir).filter(f => MIDI_EXTENSIONS.test(f));

const imports = files
  .map((f, i) => `import song${i} from "./${f}";`)
  .join('\n');

const entries = files
  .map((f, i) => {
    const name = f.replace(/\.[^/.]+$/, '');
    return `  { name: ${JSON.stringify(name)}, file: song${i} }`;
  })
  .join(',\n');

const output = `// Auto-generated by scripts/generate-music-manifest.js — do not edit manually\n${imports}\n\nexport default [\n${entries}\n];\n`;

fs.writeFileSync(outputFile, output);
console.log(`Generated src/music/manifest.js with ${files.length} song(s)`);
